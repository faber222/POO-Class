package engtelecom.poo;

/**
 * Esta classe representa uma calculadora
 */
public class Calculadora {
    /**
     * Faz a soma de dois inteiros
     * 
     * @param a é o operando da soma
     * @param b é o segundo operando da soma
     * @return retorna o valor da soma
     */
    public int somar(int a, int b) {
        return a + b;
    }

    public int diminuir(int a, int b) {
        return a - b;
    }
}
